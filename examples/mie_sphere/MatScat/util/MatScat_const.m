c0 = 299792458;             % speed of light in vacuum
mue0 = 4.*pi*1e-7;          % Permeability of free space
eps0 = 1./c0^2/mue0;        % Permittivity of free space
Z0 = sqrt(mue0/eps0);       % Impedance of free space